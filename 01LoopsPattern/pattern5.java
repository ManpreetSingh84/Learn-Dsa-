
public class pattern5{
    public static void main(String args[]){
        for(int i = 1 ; i <=4; i++){
            for(int j = 2; j<=i; j++){
                System.out.print(" ");
            }
            // System.out.println(" ");
            for(int l = 1; l<=1; l++){
                System.out.print("*");
            }
            System.out.println();
        }
      
    }
}